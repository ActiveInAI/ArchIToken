export function adapterSourceByUrl(url: string): AdapterSource | undefined {
  return adapterSourceRegistry.find((source) => source.url === url);
}

export function adapterSourceById(id: string): AdapterSource | undefined {
  return adapterSourceRegistry.find((source) => source.id === id);
}

export function normalizeAdapterExtension(value: string): string {
  const trimmed = value.trim().toLowerCase();
  if (!trimmed) return '';
  if (trimmed.startsWith('.')) return trimmed;
  const dot = trimmed.lastIndexOf('.');
  return dot >= 0 ? trimmed.slice(dot) : `.${trimmed}`;
}

export function adapterRequirementForExtension(
  extension: string,
): FormatAdapterRequirement | undefined {
  const ext = normalizeAdapterExtension(extension);
  return formatAdapterRequirements.find((requirement) =>
    requirement.extensions.includes(ext),
  );
}

export function requiredAdaptersForFileName(
  fileName: string,
  mimeType?: string,
): readonly string[] {
  const requirement = adapterRequirementForExtension(fileName);
  if (requirement) return requirement.adapters;

  const mime = mimeType?.toLowerCase() ?? '';
  if (mime.startsWith('image/')) {
    return ['browser image viewer', 'PaddleOCR worker'];
  }
  if (mime.startsWith('video/')) {
    return ['browser video viewer', 'transcode worker'];
  }
  if (mime.startsWith('audio/')) {
    return ['browser audio viewer', 'ASR/TTS provider'];
  }
  if (mime === 'application/pdf') {
    return ['browser PDF viewer', 'Stirling PDF service'];
  }

  return ['registered parser/viewer worker'];
}

export function adapterSourcesForFileName(fileName: string): AdapterSource[] {
  const requirement = adapterRequirementForExtension(fileName);
  if (!requirement) return [];

  return requirement.sourceIds
    .map(adapterSourceById)
    .filter((source): source is AdapterSource => Boolean(source));
}
