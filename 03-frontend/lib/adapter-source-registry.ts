export type AdapterSourceDecision =
  | 'selected'
  | 'selected_external_process'
  | 'licensed_gated'
  | 'reference_only'
  | 'sample_data'
  | 'blocked_pending_license';

export type AdapterCapability =
  | 'import'
  | 'export'
  | 'online_edit'
  | 'ai_generate'
  | 'validate'
  | 'sync'
  | 'automation'
  | 'viewer'
  | 'ocr'
  | 'document_conversion';

export type AdapterSourceCategory =
  | 'standards'
  | 'openbim'
  | 'cad'
  | 'bim_authoring'
  | 'document'
  | 'erp'
  | 'sync'
  | 'ai_generation'
  | 'mcp'
  | 'sample_data'
  | 'visualization';

export type AdapterSourceState =
  | 'active'
  | 'archived'
  | 'organization'
  | 'external';

export interface AdapterSource {
  id: string;
  url: string;
  label: string;
  category: AdapterSourceCategory;
  decision: AdapterSourceDecision;
  state: AdapterSourceState;
  license: string | null;
  formats: readonly string[];
  capabilities: readonly AdapterCapability[];
  note: string;
}

export interface FormatAdapterRequirement {
  family: string;
  extensions: readonly string[];
  adapters: readonly string[];
  sourceIds: readonly string[];
  importRoute: 'direct_browser' | 'worker' | 'service' | 'licensed_service';
  onlineEditRoute: 'native' | 'worker' | 'service' | 'licensed_service';
  aiGenerationRoute: 'worker' | 'provider' | 'licensed_service';
  exportRoute: 'direct' | 'worker' | 'service' | 'licensed_service';
  status: 'available' | 'adapter_required' | 'licensed_adapter_required';
}

